# Condicional While  mientras que nuestra variable conteo tenga un valor menor que 9 
# entonces que imprima por pantalla el valor de conteo. Como el valor de conteo es  = 0  
# irá a  --- conteo += 1 e irá sumando en bucle +1 la cifra anterior hazta que llegue a 8 incluido.

conteo = 0
while (conteo < 9):
    print('El conteo es: ', conteo)
    conteo +=1
print("Conteo completo!")

