#Operador de Asignacion igual
numero  = 5
print("Valor de numero = ", numero)

#Operación de asignacion más igual
num = 10
print("Valor de numero = ",num)
num += 5
print("Valor de numero = ",num)
num = num +5
print(num)

# Operador de Asignacio menos igual.
num = 30
print("Valor de numero = ",num)
num -= 5
print("Valor de numero = ",num)
num = num - 1
print(num)

#Operador de asignacio *=
num = 2
print("Valor de numero", num)
num *= 30
print("Valor de numero", num)

#Operador de asignación entre igual //=

num = 100
print("Valor de numero", num)
num //= 2
print("Valor de numero", num)